public class Operators {
     public static int sum(int num1,int num2){
         return num1+num2;
     }

    public static int subt(int num1,int num2){
        return num1-num2;
    }

    public static int mul(int num1,int num2){
        return num1*num2;
    }

    public static int div(int num1,int num2){
        return num1/num2;
    }
}
